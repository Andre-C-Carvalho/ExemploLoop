let quantidadeDeAlunos = 10
for (let contador = 0; contador <= quantidadeDeAlunos; contador++) {
    if (contador === 0) {
        console.log("O número atual é ZERO")
    } else if (contador % 2 === 0){
        console.log("O número atual é: " + contador + " (PAR)")
    } else {
        console.log("O número atual é: " + contador + " (ÍMPAR)")
    }
}

